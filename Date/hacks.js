/**
 * how many seconds does a minute have?
 */

let min = 1000 * 60;
console.log("Milliseconds in a minute: "+min)
/**
 * how many seconds does a hour have?
 */
let hour = min * 60;
console.log("Milliseconds in an hour: "+hour);

/**
 * how many seconds does a day have?
 */
let day = hour * 24;
console.log("Milliseconds in a day: "+day);

/**
 * how many seconds does a year have?
 */
let year = day * 365;
console.log("Milliseconds in a year: "+year);